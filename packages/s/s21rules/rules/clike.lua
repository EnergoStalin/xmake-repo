rule('clike')
  add_deps('mode.release', 'mode.debug', 'mode.tsan', 'mode.asan')
  on_config(function(target)
    target:set('cflags', '-Wall', '-Werror', '-Wextra', '-pedantic')
    target:set('toolchains', 'gcc')
  end)
rule_end()

rule('cxx')
  add_deps('clike')
  on_config(function(target)
    target:set('languages', 'cxx17')
  end)
rule_end()

rule('c')
  add_deps('clike')
  on_config(function(target)
    target:set('languages', 'c11')
  end)
rule_end()
